    private void procurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procurarActionPerformed
        // TODO add your handling code here:
        LocatarioDao dao = new LocatarioDao();
        Locatario cliente = dao.buscar(cpfCliente.getText());
        
        cnhCliente.setText(Integer.toString(cliente.getCnh()));
        validadeCnh.setText(cliente.dataParaString(cliente.getValidadeCnh()));
        categoraCnh.setText(cliente.getCatCnh());
        locatarioNome.setText(cliente.getNome());
        
        if(cliente.verificarValidade(cliente.getValidadeCnh())){
            fazerReserva.setEnabled(false);
        }
        
    }//GEN-LAST:event_procurarActionPerformed

